int	ft_sqrt(int nb)
{
	int	count;
	int	p;

	count = 0;
	p = 1;
	while (nb > 0)
	{
		nb = nb - p;
		p += 2;
		count++;
	}
	if (nb == 0)
		return (count);
	else
		return (0);
}
